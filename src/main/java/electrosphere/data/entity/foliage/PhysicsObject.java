package electrosphere.data.entity.foliage;

/**
 * Physics data
 */
public class PhysicsObject {
    
    String type;
    
    float dimension1;
    float dimension2;
    float dimension3;
    
    float offsetX;
    float offsetY;
    float offsetZ;

    public String getType() {
        return type;
    }

    public float getDimension1() {
        return dimension1;
    }

    public float getDimension2() {
        return dimension2;
    }

    public float getDimension3() {
        return dimension3;
    }

    public float getOffsetX() {
        return offsetX;
    }

    public float getOffsetY() {
        return offsetY;
    }

    public float getOffsetZ() {
        return offsetZ;
    }
    
    
    
}
